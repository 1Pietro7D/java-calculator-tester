package feb09;

public class Calculator {
	private float num1;
	private float num2;

	public float add(float num1, float num2) {
		return num1 + num2;
	}

	public float subtract(float num1, float num2) {
		return num1 - num2;
	}

	public float multiply(float num1, float num2) {
		return num1 * num2;
	}

	public float divide(float num1, float num2) {
		return num1 / num2;
	}
}